CREATE TABLE KoKo_DBOne.PERSON_COMMENT (
    personCommentId INT AUTO_INCREMENT PRIMARY KEY,
    personPostId INT,
    userId INT,
    userName VARCHAR(255),
    comment VARCHAR(500),
    FOREIGN KEY (personPostId) REFERENCES PERSON_POST(personPostId) ON DELETE CASCADE,
    FOREIGN KEY (userId) REFERENCES PERSON(userId) ON DELETE CASCADE,
    FOREIGN KEY (userName) REFERENCES PERSON(userName) ON DELETE CASCADE
);